package in.reqres.pojo_classes;

public class Support {

    private String url;
    private String text;

    public Support(String url, String text) {
        this.url = url;
        this.text = text;
    }

    public Support() {

    }

    public String getUrl() {
        return url;
    }

    public String getText() {
        return text;
    }

}
